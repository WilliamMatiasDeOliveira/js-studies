let array = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]

// exemplo de for in
// O n vai retornar a posição do elemento
for(n in array){
    console.log(n);
}

// exemplo de for of
// O n retorna o elemento do array
for(n of array){
    console.log(n);
    
}

